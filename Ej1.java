//Declara dos variables numéricas (con el valor que desees), muestra por consola la suma, resta, multiplicación, división y módulo (resto de la división)
public class Ej1{  
  public static void main(String args[]){    
   int num1 = 8;
   int num2 = 4;

   int suma= num1+num2;
    System.out.println("La suma es: "+suma); 

    int resta= num1-num2;
    System.out.println("La resta es: "+resta); 

    int multiplicación= num1*num2;
    System.out.println("La multiplicacion es: "+multiplicación); 

    int división= num1/num2;
    System.out.println("La division es: "+división); 

    int módulo= num1%num2;
    System.out.println("El modulo es: "+módulo); 
  }  
}